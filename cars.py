cars = ['bmw', 'audi', 'toyota', 'subaru']
#The sort method changes the list order PERMANENTLY
cars.sort()
print(f"Printing cars in alphabetical order{cars}")

cars.sort(reverse=True)
print(f"Printing cars in reverse order{cars}")

numbers = [1,9,7,8]
numbers.sort()
print(f"Printing numbers in order{numbers}")

numbers.sort(reverse=True)
print(f"Printing numbers in reverse order{numbers}")

# when working with divisions you'll always get a float
print(4/2)

#if you mix integers and floats you'll also get a floar
print(1*3.0)

#to make big numbers more readable you can use underscores
universe_age = 14_000_000_000
print(f"{universe_age} is the age of the Universe.")

#you can use multiple assignment
x,y,z = 1,2,3

print(z,x,y)

#and finally, this is a constant
MAX_VALUE = 50_000_000_000_000_000

print(f"And our maximum value is: {MAX_VALUE}")
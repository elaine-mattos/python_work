name_1 = "ada lovelace"
name_2 = "ADA LOVELACE"
name_3 = "aDa lOVELACe"

#Names case
print("Displaying names in 'Name case'")
print("Name before: ", name_1, ". Name after: ", name_1.title())
print("Name before: ", name_2, ". Name after: ", name_2.title())
print("Name before: ", name_3, ". Name after: ", name_3.title())

#Upper Case
print("---------------------------------------------------")
print("Displaying names in 'Upper case'")
print("Name before: ", name_1, ". Name after: ", name_1.upper())
print("Name before: ", name_2, ". Name after: ", name_2.upper())
print("Name before: ", name_3, ". Name after: ", name_3.upper())

#Lower Case
print("---------------------------------------------------")
print("Displaying names in 'Lower case'")
print("Name before: ", name_1, ". Name after: ", name_1.lower())
print("Name before: ", name_2, ". Name after: ", name_2.lower())
print("Name before: ", name_3, ". Name after: ", name_3.lower())
